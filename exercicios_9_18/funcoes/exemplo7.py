# Crie uma funcao que conte a quantidade de caracteres de uma frase
def conta_caracteres_frase(frase):
    qtd_caracteres = len(frase)

    return qtd_caracteres


resposta = conta_caracteres_frase("Buritizeiro Minas Gerais")
print(resposta)

resposta = conta_caracteres_frase("Pirapora Minas Gerais")
print(resposta)

resposta = conta_caracteres_frase("Programacao com Python")
print(resposta)

